using System;

namespace DataTypeTest
{
	class DataTypeTest
	{
		static void Main()
		{
			// int型で演算を行う
			int a = 10 / 3;
			Console.WriteLine(a);

			// double型で演算
			double b = 10.0 / 3.0;
			Console.WriteLine(b);

			// char型を表示
			char c = 'A';
			Console.WriteLine(c);

			// ushort型を表示
			ushort d = 65;
			Console.WriteLine(d);

		}
	}
}
